    public void formatPath()
    {
        int currentIndex = playerPosition;
        chaserPathway.Push(currentIndex);

        while (currentIndex != chaserPosition)
        {
            chaserPathway.Push(parents[currentIndex]);
            currentIndex = parents[currentIndex];
        }
    }

    public void dijkstra()
    {
        parents = new int[V];
        int src = chaserPosition;

        int[] distance = new int[V];
        for (int i = 0; i < V; i++) { distance[i] = int.MaxValue; }
        distance[src] = 0;
        parents[src] = NO_PARENT;

        Queue<AdjListNode> pq = new Queue<AdjListNode>();
        pq.Enqueue(new AdjListNode(src, 0));

        while (pq.Count > 0)
        {
            AdjListNode current = pq.Dequeue();
            foreach (AdjListNode n in graph[current.getVertex()])
            {
                if (distance[current.getVertex()] + n.getWeight() < distance[n.getVertex()])
                {
                    distance[n.getVertex()] = n.getWeight() + distance[current.getVertex()];
                    pq.Enqueue(new AdjListNode(n.getVertex(), distance[n.getVertex()]));
                    parents[n.getVertex()] = current.getVertex();
                }
            }
        }
    }
